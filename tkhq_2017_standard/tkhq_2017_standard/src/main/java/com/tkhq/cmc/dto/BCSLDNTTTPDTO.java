package com.tkhq.cmc.dto;

public class BCSLDNTTTPDTO {
	public BCSLDNTTTPDTO(String ma_tinh, String tentinhtp, long so_luong,
			long nam) {
		super();
		this.ma_tinh = ma_tinh;
		this.tentinhtp = tentinhtp;
		this.so_luong = so_luong;
		this.nam = nam;
	}
	public String getMa_tinh() {
		return ma_tinh;
	}
	public String getTentinhtp() {
		return tentinhtp;
	}
	public long getSo_luong() {
		return so_luong;
	}
	public long getNam() {
		return nam;
	}
	private String ma_tinh;
	private String tentinhtp;
	private long so_luong;
	private long nam;
	
}
