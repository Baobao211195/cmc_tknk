package com.tkhq.cmc.dto;

public class QuanLyToChucDTO {
	
	private String tenDangNhap;
	
	private String tenDayDu;
	
	private String maHaiQuan;
	
	private String tenDonvi;
	
	private String nhom;
	
	private String trangThai;
	
	/**
	 * @return the tenDangNhap
	 */
	public String getTenDangNhap() {
		return tenDangNhap;
	}

	/**
	 * @param tenDangNhap the tenDangNhap to set
	 */
	public void setTenDangNhap(String tenDangNhap) {
		this.tenDangNhap = tenDangNhap;
	}

	/**
	 * @return the tenDayDu
	 */
	public String getTenDayDu() {
		return tenDayDu;
	}

	/**
	 * @param tenDayDu the tenDayDu to set
	 */
	public void setTenDayDu(String tenDayDu) {
		this.tenDayDu = tenDayDu;
	}

	/**
	 * @return the maHaiQuan
	 */
	public String getMaHaiQuan() {
		return maHaiQuan;
	}

	/**
	 * @param maHaiQuan the maHaiQuan to set
	 */
	public void setMaHaiQuan(String maHaiQuan) {
		this.maHaiQuan = maHaiQuan;
	}

	/**
	 * @return the tenDonvi
	 */
	public String getTenDonvi() {
		return tenDonvi;
	}

	/**
	 * @param tenDonvi the tenDonvi to set
	 */
	public void setTenDonvi(String tenDonvi) {
		this.tenDonvi = tenDonvi;
	}

	/**
	 * @return the nhom
	 */
	public String getNhom() {
		return nhom;
	}

	/**
	 * @param nhom the nhom to set
	 */
	public void setNhom(String nhom) {
		this.nhom = nhom;
	}

	/**
	 * @return the trangThai
	 */
	public String getTrangThai() {
		return trangThai;
	}

	/**
	 * @param trangThai the trangThai to set
	 */
	public void setTrangThai(String trangThai) {
		this.trangThai = trangThai;
	}

	public QuanLyToChucDTO(String tenDangNhap, String tenDayDu,
			String maHaiQuan, String tenDonvi, String nhom, String trangThai) {
		super();
		this.tenDangNhap = tenDangNhap;
		this.tenDayDu = tenDayDu;
		this.maHaiQuan = maHaiQuan;
		this.tenDonvi = tenDonvi;
		this.nhom = nhom;
		this.trangThai = trangThai;
	}
	
	
}
