package com.tkhq.cmc.model;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;




@Entity
@Table (name="TBS_CHUYENDE")
public class Tbs_ChuyenDe {
	
	@Id
	@Column(name = "MA_CD")
	private	String maCDe;
	
	@Column(name = "TEN_CD")
	private	String tenCDe;
	
	@Column(name = "MA_HANG")
	private	String maHang;
	
	@Column(name = "MOTA_HANGHOA")
	private	String motaHH;

	public Tbs_ChuyenDe () {
		
	}
	public String getMaCDe() {
		return maCDe;
	}

	public void setMaCDe(String maCDe) {
		this.maCDe = maCDe;
	}

	public String getTenCDe() {
		return tenCDe;
	}

	public void setTenCDe(String tenCDe) {
		this.tenCDe = tenCDe;
	}

	public String getMaHang() {
		return maHang;
	}

	public void setMaHang(String maHang) {
		this.maHang = maHang;
	}

	public String getMotaHH() {
		return motaHH;
	}

	public void setMotaHH(String motaHH) {
		this.motaHH = motaHH;
	}

}
