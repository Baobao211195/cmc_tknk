public class DonviHQ{

	public DonviHQ(String ma, String ten) {
		super();
		this.ma = ma;
		this.ten = ten;
	}
	private String ma;
	public String getMa() {
		return ma;
	}
	public void setMa(String ma) {
		this.ma = ma;
	}
	public String getTen() {
		return ten;
	}
	public void setTen(String ten) {
		this.ten = ten;
	}
	private String ten;
}
